export default function TechStack() {
  const sectionRef = useRef(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.2, rootMargin: "0px 0px -80px 0px" }
    );
    if (sectionRef.current) observer.observe(sectionRef.current);
    return () => observer.disconnect();
  }, []);

  // ---------- FLATTEN & BALANCED PYRAMID ----------
  const allItems = TECH_CATEGORIES[0].items;
  const N = allItems.length;

  // Find largest k with sum 1..k ≤ N
  const k = Math.floor((Math.sqrt(8 * N + 1) - 1) / 2);
  const baseSum = (k * (k + 1)) / 2;
  let leftover = N - baseSum;

  // Start with perfect pyramid [k, k-1, ..., 1] (top to bottom)
  const rowSizes = Array.from({ length: k }, (_, i) => k - i);

  // Distribute leftovers to the top rows (one per row)
  let idx = 0;
  while (leftover > 0 && idx < rowSizes.length) {
    rowSizes[idx] += 1;
    leftover--;
    idx++;
  }

  // If there are still leftovers, add a new row at the top (rare)
  while (leftover > 0) {
    rowSizes.unshift(1);
    leftover--;
  }

  // Slice items into rows
  const rows = [];
  let start = 0;
  for (const size of rowSizes) {
    rows.push(allItems.slice(start, start + size));
    start += size;
  }

  return (
    <section
      ref={sectionRef}
      id="skills"
      className="relative overflow-hidden py-16 sm:py-20"
    >
      {/* Background glow only (no white dots) */}
      <div className="pointer-events-none absolute inset-0">
        <div className="absolute -top-40 left-1/2 h-96 w-[120%] -translate-x-1/2 rounded-full tech-stack-glow" />
      </div>

      <Container>
        <div className="relative flex flex-col items-start gap-8 sm:gap-10 md:gap-12">
          <div
            className="w-full transition-all duration-700 ease-out"
            style={{
              opacity: isVisible ? 1 : 0,
              transform: isVisible ? "translateY(0)" : "translateY(24px)",
            }}
          >
            <SectionHeading
              kicker="03. TECH STACK"
              title="Tech"
              accent="Stack"
              align="center"
              description="A compact snapshot of the tools I use to build products—from UI to ML."
            />
          </div>

          {/* Mobile-first compact grid (<=375px optimized) */}
          <div className="w-full sm:hidden">
            <div className="mx-auto grid w-full max-w-[420px] grid-cols-3 gap-2 min-[360px]:grid-cols-4">
              {allItems.map((item, i) => (
                <TechCard key={item.name} item={item} index={i} isVisible={isVisible} />
              ))}
            </div>
          </div>

        {/* ---------- BALANCED PYRAMID GRID ---------- */}
        <div className="relative hidden w-full rounded-2xl sm:block">
          <div className="" />

          <div className="relative flex flex-col  items-center gap-4 sm:gap-5 md:gap-6">
            {rows.map((rowItems, rowIndex) => (
              <div
                key={rowIndex}
                className="flex flex-wrap justify-center w-full gap-2 sm:gap-3 transition-all duration-700"
                style={{
                  opacity: isVisible ? 1 : 0,
                  transform: isVisible ? "translateY(0)" : "translateY(16px)",
                  transitionDelay: `${rowIndex * 80}ms`,
                }}
              >
                {rowItems.map((item, itemIndex) => {
                  const globalIndex = rows
                    .slice(0, rowIndex)
                    .reduce((sum, r) => sum + r.length, 0) + itemIndex;
                  return (
                    <TechCard
                      key={item.name}
                      item={item}
                      index={globalIndex}
                      isVisible={isVisible}
                    />
                  );
                })}
              </div>
            ))}
          </div>
        </div>
        </div>
      </Container>
    </section>
  );
}
